package ch.hslu.ad.sw01.E1;

public class ahaCounter {

    private int count;
    public ahaCounter() {
        this.count = count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public void setCountPlusOne(){
        this.count++;
    }

    public int getCount() {
        return count;
    }
}
