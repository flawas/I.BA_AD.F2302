package ch.hslu.ad.sw01.Ackermann;

public class AckermannCounter {

    // Variablen
    private int count;

    public AckermannCounter() {
        this.count = count;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public void setCountPlusOne(){
        this.count = this.count + 1;
    }
}
